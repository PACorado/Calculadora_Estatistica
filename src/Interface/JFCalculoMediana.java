    private void jButtonAddNumeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddNumeroActionPerformed
        l.inserirMediana(Float.parseFloat(jTextFieldgetNumero.getText()));
        if (l.getContagemElementos() == 1) {
            jTextFieldMediana.setText(l.retornaElemento(1).toString());
            jTextAreaNumerosDigitados.append(jTextFieldgetNumero.getText());
            jTextFieldgetNumero.setText(null);
            return;
        }

        if (l.getContagemElementos() > 1) {
            jTextAreaNumerosDigitados.append("," + jTextFieldgetNumero.getText());
            jTextFieldMediana.setText(null);
        } else {
            jTextAreaNumerosDigitados.append(jTextFieldgetNumero.getText());
        }
        if ((l.getContagemElementos() % 2) == 1) {
            Float aux = (l.retornaElemento((l.getContagemElementos() + 1) / 2));
            jTextFieldMediana.setText(aux.toString());
        } else {
        Float aux = (l.retornaElemento((l.getContagemElementos() / 2), ((l.getContagemElementos() / 2) + 1)));
            jTextFieldMediana.setText(aux.toString());
        }

        jTextFieldgetNumero.setText(null);


    }//GEN-LAST:event_jButtonAddNumeroActionPerformed

